//Creating Array

let petanimals:string[] = ["Dog","Cat","Horse"];

//Using For-Loop

for(let animals of petanimals){   //yahan animals ka variable one by one kar kay petanimals waly array se print karega with message.
    console.log(`A ${animals} would make a great pet.`); 
}


//Print message outside the for loop
console.log("\nAny of these animals would make a great pet ! ");



